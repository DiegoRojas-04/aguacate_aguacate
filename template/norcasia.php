    <div class="texto-centrado">
            <p>En el año de 1908 el Municipio de Samaná ya reconocía el asentamiento poblacional de Norcasia como tal, gracias a sus riquezas y atractivos, pues llegaron personajes de Sonsón, Abejorral, Manzanares y Pensilvania, aventureros que venían en busca de caucho, oro y caza para mejorar sus condiciones de vida. Este centro poblado enclavado en la margen derecha del río La Miel y en gran parte en la del río Moro, fue fundado por Jesús María Carvajal, Dimas Gómez, Antonio Valencia, Santiago Gallego y José Gallego Arias; los datos de sus primeros pobladores datan de 1924, fecha en la cual se le dio su nombre, Norcasia, debido a que se encontraba casi al norte de San Agustín, lugar del cual los Españoles venían con cargamentos, necesitando un lugar donde descansar. Fue proclamado como corregimiento del Municipio de Samaná el 30 de junio en 1938, por acuerdo del Concejo Municipal de Samaná; coincidencialmente; el 30 de junio de 1999 mediante la Ordenanza No. 327 de la Asamblea de Caldas, y ratificado mediante referéndum el 15 de agosto de 1999 se eleva a la categoría de Municipio. El proceso de poblamiento de esta región, vivió una serie de oleadas producto de la apertura de la frontera minera y agropecuaria y la búsqueda de una salida hacia el Magdalena (principal vía de comunicación de ese momento); de la expansión del café y la ganadería, basada en núcleos de población expulsados por el proyecto ético - político antioqueño y por las guerras de fin del siglo XIX y principios del siglo XX, con todas las consecuencias de violencia política y escasez de recursos para subsistir, que buscaron en el Magdalena Medio Caldénse y en la zona de San Miguel una alternativa de sobrevivencia. En general, la zona presenta una alta movilidad poblacional y la tendencia es a ello. Las causas son muy diversas pero todas relacionadas con una profunda crisis agrícola, tanto en la región como en el país. </p>
