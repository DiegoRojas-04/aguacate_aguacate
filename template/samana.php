            <p>La historia del territorio comienza con la Tribu Pantágoras, descendiente de los indios Caribes, que habitaban extensas zonas selváticas de faldas empinadas, picachos inaccesibles, cordilleras estrechas, llanuras ardiente y valles profundos que conformaban los terrenos donde ejercían su autonomía, donde la puerta de entrada era el río Guarinó, el cual se convertía en el punto de partida a su gran extensión territorial que atravesaba otros ríos como Samaná Sur, La Miel, San Carlos, Samaná Norte y Nare, hasta la cercanía del Río San Bartolomé.

Los indios Pantágoras se caracterizaban por ser guerreros, donde en defensa de sus posesiones se enfrentaban con alguna regularidad a sus vecinos los Panches, Los Gualíes y Los Colimas, los cuales eran indios valientes que se enfrentaban en combates feroces. Por lo extenso de sus territorios se les llego a llamar Nación Pantágoras, puesto que al compararlos con otras tribus como los Quimbayas y Pijaos, estos dominaban en terrenos dos o tres veces mayores.

Bajo su dinámica se formaron otros grupos como Los palenques, Los Amaníes, Samanáes (que se instalaron dentro del Municipio) y Marquetones, que se fueron asentando por todo el Oriente Caldense, incluidos parte de terrenos pertenecientes al Departamento del Tolima.</p>
