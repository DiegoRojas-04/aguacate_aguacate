El territorio que hoy ocupa el municipio de Victoria estaba habitado al tiempo del descubrimiento y la conquista por las tribus o familias de los palenques y pantagoras, los cuales formaban parte de la gran nación de los tahamies, descendientes de los caribes . Fundaciones En la Historia de Victoria se registran cinco fundaciones así: 1553. Fundada en territorios pertencecientes hoy al Municipio de Samaná, en cercanias de los Rios La Miel y Manso.Por diversas circunstancias, la fundación duro poco tiempo.Dos intentos mas se llevaron a cabo, con resultados igualmente negativos.El último de éstos ustuvo ubicado en la desembocadura del rio Guarinó en el Magdalena.Los gestores de estas empresas colonizadores fueron expedicionarios Españoles encabezados por Hernando de Salinas. 1.840.La cuarta fundación, la cual también se llamo Victoria, se origino en la región de San Mateo, parte alta de la Montaña de Bellavista.Fue impulsada por las corrientes migratorias procedentes, en sus origenes de Antioquia La Grande, reforzadas con inmigrantes del Tolima, de Cundinamarca y los Santanderes. 1.879. La escasez de agua potable y los fuertes vendabales que frecuntemente azotaban la región, obligaron a los moradores del incipiente poblado a buscar un sitio que ofreciera mejores condiciones de vida.Después de afanosas exploraciones, descubrieron al pie de la Montaña de Bellavista unos excelentes terrenos, con abundantes aguas y materiales para la construcción de las viviendas.Fué asi como a finales de este año en diciembre 23 de 1.879, concluyeron el translado de todas sus pertenencias al lugar donde hoy esta ubicada la Población.Fue ésta la quinta y última fundación de Victoria. 1.887. En Octubre 13 de éste año, el Gobernador del Tolima, Manuel Casabianca, dicto el decreto No 650 por medio del cual la aldea de Victoria y otras, fueron elevadas a la categoría de Distritos, ( Hoy Municipios). 1.905. Fue creado el Depto de Caldas, con territorios segregados a Antioquia, Tolima, Choco y 